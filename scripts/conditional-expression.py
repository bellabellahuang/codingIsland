# regular if-else statement
if condition:
    # do something when condition is true
    statement_1
    statement_2
    ...
    statement_n
else:
    # do something when condition is false
    statement_1
    statement_2
    ...
    statement_n


# one line statement
m = a if condition else b

if condition: return something


# Guard Clause
if condition_A:
    return result_A
if condition_B:
    return result_B
if condition_C:
    return result_C
return default_result
